// Initialize language from localStorage or default to 'ko'
window.currentLang = localStorage.getItem('travel_traffic_lang') || 'ko';

// Helper to get translated string with fallback
window.t = function(key, fallback = '') {
    const lang = window.currentLang || 'ko';
    if (window.translations && window.translations[lang] && window.translations[lang][key]) {
        return window.translations[lang][key];
    }
    return fallback;
};

function setLanguage(lang) {
    if (!window.translations || !window.translations[lang]) return;
    window.currentLang = lang;
    localStorage.setItem('travel_traffic_lang', lang);

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translatedText = window.t(key);
        if (translatedText) {
            if (element.tagName.toLowerCase() === 'input' && element.type === 'text') {
                element.placeholder = translatedText;
            } else {
                element.innerHTML = translatedText;
            }
        }
    });

    // Update tags
    document.querySelectorAll('[data-i18n-tags]').forEach(element => {
        const key = element.getAttribute('data-i18n-tags');
        const tagsString = window.t(key);
        if (tagsString) {
            const tags = tagsString.split(',').filter(t => t.trim() !== '');
            element.innerHTML = tags.map(tag => `<span>${tag.startsWith('#') ? tag : '#' + tag}</span>`).join('');
        }
    });

    // Update images
    document.querySelectorAll('[data-i18n-src]').forEach(element => {
        const key = element.getAttribute('data-i18n-src');
        const src = window.t(key);
        if (src) {
            element.src = src;
            if (element.parentElement.classList.contains('guide-image-container')) {
                element.parentElement.style.display = 'block';
            }
        } else {
            if (element.parentElement.classList.contains('guide-image-container')) {
                element.parentElement.style.display = 'none';
            }
        }
    });

    // Update language selector dropdowns
    document.querySelectorAll('#lang-selector').forEach(sel => {
        sel.value = lang;
    });

    // Custom event for dynamic parts like cards
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

async function syncGuidesFromDatabase() {
    try {
        console.log('Syncing guides from database...');
        const res = await fetch('/api/guides?t=' + Date.now());
        if (!res.ok) {
            console.warn('Guide sync failed:', res.status);
            return;
        }
        const guides = await res.json();
        
        guides.forEach(g => {
            ['ko', 'en', 'ja'].forEach(lang => {
                const data = g[lang];
                if (data && window.translations[lang]) {
                    window.translations[lang][`guide.${g.id}.title`] = data.title;
                    window.translations[lang][`guide.${g.id}.desc`] = data.desc;
                    window.translations[lang][`guide.${g.id}.content`] = data.content;
                    window.translations[lang][`guide.${g.id}.tags`] = data.tags;
                    window.translations[lang][`guide.${g.id}.image`] = data.image;
                }
            });
        });
        
        console.log('Guide sync complete. Refreshing UI...');
        // Refresh UI with new data
        setLanguage(window.currentLang);
    } catch (e) {
        console.error('Failed to sync guides from database:', e);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    // Detect stored lang
    const savedLang = localStorage.getItem('travel_traffic_lang') || 'ko';
    
    // Add event listeners to all selectors
    document.querySelectorAll('#lang-selector').forEach(sel => {
        sel.addEventListener('change', (e) => setLanguage(e.target.value));
    });

    // Initial run with static data
    setLanguage(savedLang);

    // Sync with live data from database
    syncGuidesFromDatabase();
});
